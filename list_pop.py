'''The list pop() method removes the item at the specified index.
 The method also returns the removed item.'''

music_instruments = ['piano','guitar','drum','violin','cello']

''' remove the element at index 2'''
removed_element = music_instruments.pop(2)
'''print'''

print('Removed Element:', removed_element)
print('Updated List:', music_instruments)

'''Output: 
Removed Element: drum
Updated List: ['piano', 'guitar', 'violin', 'cello']'''

